#!/usr/bin/env node
/**
 * Export all Supabase tables to local JSON + SQL seed files.
 *
 * Usage:
 *   cp .env.example .env   # fill in SUPABASE_URL and SUPABASE_SERVICE_ROLE_KEY
 *   npm run db:export
 *   npm run db:export -- --strip-passwords   # omit password hashes when sharing
 */

import { createClient } from '@supabase/supabase-js'
import { config } from 'dotenv'
import { mkdir, writeFile } from 'fs/promises'
import { dirname, join } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..')
const OUT_DIR = join(ROOT, 'database')

config({ path: join(ROOT, '.env') })

const TABLES = [
  'users',
  'teams',
  'messages',
  'team_links',
  'team_chats',
  'team_images',
  'announcements',
]

const stripPasswords = process.argv.includes('--strip-passwords')

function requireEnv(name) {
  const value = process.env[name]
  if (!value || value.startsWith('your_')) {
    console.error(`Missing or placeholder ${name} in .env`)
    console.error('Copy .env.example to .env and add your Supabase credentials.')
    process.exit(1)
  }
  return value
}

function sqlLiteral(value) {
  if (value === null || value === undefined) return 'NULL'

  if (typeof value === 'boolean') return value ? 'TRUE' : 'FALSE'
  if (typeof value === 'number') return Number.isFinite(value) ? String(value) : 'NULL'

  if (Array.isArray(value)) {
    if (value.length === 0) return "'{}'"
    const items = value.map((item) => {
      if (item === null || item === undefined) return 'NULL'
      if (typeof item === 'object') return `"${JSON.stringify(item).replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`
      return `"${String(item).replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`
    })
    return `ARRAY[${items.join(', ')}]::text[]`
  }

  if (typeof value === 'object') {
    const json = JSON.stringify(value).replace(/'/g, "''")
    return `'${json}'::jsonb`
  }

  return `'${String(value).replace(/'/g, "''")}'`
}

function quoteIdentifier(name) {
  return `"${name.replace(/"/g, '""')}"`
}

function buildInsertStatements(table, rows) {
  if (!rows.length) return `-- ${table}: no rows\n`

  const columns = Object.keys(rows[0])
  const columnList = columns.map(quoteIdentifier).join(', ')
  const lines = [`-- ${table}: ${rows.length} row(s)`, '']

  for (const row of rows) {
    const values = columns.map((col) => sqlLiteral(row[col])).join(', ')
    lines.push(
      `INSERT INTO ${quoteIdentifier(table)} (${columnList}) VALUES (${values}) ON CONFLICT DO NOTHING;`
    )
  }

  lines.push('')
  return lines.join('\n')
}

async function exportTable(supabase, table) {
  const { data, error } = await supabase.from(table).select('*')
  if (error) {
    if (error.code === '42P01' || error.message?.includes('does not exist')) {
      console.warn(`  skipped ${table} (table not found)`)
      return []
    }
    throw new Error(`${table}: ${error.message}`)
  }

  let rows = data || []

  if (table === 'users' && stripPasswords) {
    rows = rows.map(({ password, ...rest }) => rest)
  }

  return rows
}

async function main() {
  const supabaseUrl = requireEnv('SUPABASE_URL')
  const serviceRoleKey = requireEnv('SUPABASE_SERVICE_ROLE_KEY')

  const supabase = createClient(supabaseUrl, serviceRoleKey, {
    auth: { persistSession: false, autoRefreshToken: false },
  })

  await mkdir(join(OUT_DIR, 'data'), { recursive: true })

  console.log('Exporting Supabase data...')
  if (stripPasswords) console.log('(passwords will be omitted from users export)')

  const manifest = {
    exportedAt: new Date().toISOString(),
    supabaseUrl,
    stripPasswords,
    tables: {},
  }

  const exported = {}

  for (const table of TABLES) {
    process.stdout.write(`  ${table}... `)
    const rows = await exportTable(supabase, table)
    exported[table] = rows
    manifest.tables[table] = { rowCount: rows.length }
    console.log(rows.length)

    await writeFile(
      join(OUT_DIR, 'data', `${table}.json`),
      JSON.stringify(rows, null, 2) + '\n'
    )
  }

  const sqlParts = [
    '-- ICEHub seed data (auto-generated by scripts/export-supabase.mjs)',
    `-- Exported: ${manifest.exportedAt}`,
    '-- Run database/schema.sql first, then this file.',
    '',
    'BEGIN;',
    '',
  ]

  for (const table of TABLES) {
    sqlParts.push(buildInsertStatements(table, exported[table]))
  }

  sqlParts.push('COMMIT;', '')

  await writeFile(join(OUT_DIR, 'seed-data.sql'), sqlParts.join('\n'))
  await writeFile(join(OUT_DIR, 'manifest.json'), JSON.stringify(manifest, null, 2) + '\n')

  const totalRows = Object.values(manifest.tables).reduce((sum, t) => sum + t.rowCount, 0)

  console.log('')
  console.log(`Done. ${totalRows} total rows exported to database/`)
  console.log('  database/data/*.json   — machine-readable snapshots')
  console.log('  database/seed-data.sql — SQL inserts for Supabase SQL Editor')
  console.log('  database/manifest.json — export metadata')
  console.log('')
  console.log('Share the repo folder with database/ included so others can restore locally.')
}

main().catch((error) => {
  console.error('Export failed:', error.message)
  process.exit(1)
})
